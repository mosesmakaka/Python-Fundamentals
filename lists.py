#Lists in Python
#Lists are mutable
#Lists are ordered
#Lists can contain any arbitrary objects
#List elements can be accessed by index
#Lists can be nested to arbitrary depth
#Lists are dynamic
#Lists are heterogeneous
#List elements do not need to be unique
#Lists are a compound data type
#Lists are the most versatile Python data type

#List Creation
#List creation is pretty straightforward:
#list_name = [element_1, element_2, element_3, ...]
#The list elements can be anything at all, even other lists. Here are some examples:
#empty_list = []
